import pandas as pd
import numpy as np

# ==========================================
# 1. 데이터셋 로드 (Kaggle House Prices)
# ==========================================
print("=== [Step 1] 데이터셋 로딩 ===")
train_df = pd.read_csv('train.csv')

# 데이터 구조와 변수 타입, 결측치 여부를 한눈에 파악합니다.
print(f"데이터셋 형태(행, 열): {train_df.shape}")
print("-" * 50)

# ==========================================
# 2. 타겟 변수(SalePrice) 정밀 진단
# ==========================================
print("\n=== [Step 2] 타겟 변수(주택 가격) 진단 ===")
target = train_df['SalePrice']

# 주택 가격의 기초 통계량 확인
print(target.describe())
print("-" * 50)

# ==========================================
# 3. 왜도(Skewness) 해결을 위한 로그 변환 (핵심!)
# ==========================================
print("\n=== [Step 3] 타겟 변수 로그 변환 수행 ===")

# 일반 선형 회귀 모델은 타겟 데이터가 '정규분포'를 따를 때 최상의 성능을 냅니다.
# 하지만 주택 가격처럼 고가 주택이 일부 섞여 우측으로 꼬리가 긴 데이터는 로그 변환이 필수입니다.
# np.log1p(x)는 log(1 + x)를 계산하여, 혹시 데이터에 0이 있어도 에러(정의되지 않음)가 나지 않게 안전장치를 둔 함수입니다.
target_log = np.log1p(target)

print("변환 전 주택 가격 예시 (Original):")
print(target.head().tolist())

print("\n변환 후 로그 주택 가격 예시 (Log Transformed):")
print(target_log.head().tolist())
print("-" * 50)

# ==========================================
# 4. 내일 코랩(Colab) 파이프라인으로 넘겨줄 데이터 준비 완료
# ==========================================
# 예측에 쓸 특성(X)에서 학습과 무관한 'Id'와 정답인 'SalePrice'를 격리합니다.
X = train_df.drop(['Id', 'SalePrice'], axis=1)
y = target_log # 이제부터 정답지는 로그 변환된 y를 사용합니다.

print(f"\n[준비 완료] 입력 특성(X) 차원: {X.shape} | 변환된 타겟(y) 차원: {y.shape}")
